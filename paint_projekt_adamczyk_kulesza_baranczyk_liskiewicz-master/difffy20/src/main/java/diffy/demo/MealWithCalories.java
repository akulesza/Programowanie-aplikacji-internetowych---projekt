package diffy.demo;

public class MealWithCalories {
    public String food;
    public double kcal;
    public double protein;
    public double carbon;
    public double fat;

    public MealWithCalories(String food, double kcal, double protein, double carbon, double fat){
        this.food = food;
        this.kcal = kcal;
        this.protein = protein;
        this.carbon = carbon;
        this.fat = fat;
    }
}
